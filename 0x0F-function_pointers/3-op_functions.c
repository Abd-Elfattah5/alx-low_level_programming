/**
  *op_add - function  to add two integers
  *Return: the added numbers
  *@a: the first number
  *@b: the second number
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  *op_sub - function to subtracy two variables
  *Return: the subtracted two numbers
  *@a: first number
  *@b: second number
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  *op_mul - function to multiply two variables
  *Return: the multiplied two numbers
  *@a: first number
  *@b: second number
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  *op_div - function to divid two variables
  *Return: the divided two numbers
  *@a: first number
  *@b: second number
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  *op_mod - function to get modules of two variables
  *Return: the module of two numbers
  *@a: first number
  *@b: second number
  */
int op_mod(int a, int b)
{
	return (a % b);
}



